#include "main.h"
#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>
#include <string.h>

/**
 * _strcat - This function concatenates two strings
 * @dest: the destination string
 * @src: the target string
 *
 * Return: returns dest
 */
char *_strcat(char *dest, char *src)
{
	return strcat(dest, src);
}

