#include <stdlib.h>
#include <ctype.h>
#include <string.h>

/**
 * _strcpy - copies string from one pointer to the other
 * @dest: string one
 * @src: string two
 *
 * Return: returns a string of characters
 */
char *_strcpy(char *dest, char *src)
{
    return strcpy(dest, src);
}

